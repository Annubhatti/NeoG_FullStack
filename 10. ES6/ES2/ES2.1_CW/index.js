//Arrow Fun: it is a shorter way of write fun as compare to normal fun

//Traditional Way of writing functions
function add(a,b){
    return a+b;
}
console.log(add(5,2));

// Arrow Functions ES6 way
const add = (a,b) => a+b
console.log(add(5,2));

// mutiply two number
const multiply = (a,b) => a * b
console.log(multiply(2,5));

// concatinate two string
const concatinateStrings = (str1, str2) => str1 + str2
console.log(concatinateStrings("Hello", "World!"));




